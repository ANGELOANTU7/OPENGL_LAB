A = [[12, 7, 3],
	[4, 5, 6],
	[7, 8, 9]]


B = [[5, 8, 1],
	[6, 7, 3],
	[4, 5, 9]]

C = [[0, 0, 0],
	[0, 0, 0],
	[0, 0, 0]]


result = [[sum(a * b for a, b in zip(A_row, B_col))
						for B_col in zip(*B)]
								for A_row in A]
for i in range (0,len(A)):
	for j in range (0,len(A[i])):
		C[i][j]=A[i][j]+B[i][j]

print("Multiplication")
for r in result:
	print(r)
print("Addition")
print(C)


